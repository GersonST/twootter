<template>
  <div id="app">{{ user.username }} - {{ fullName }}</div>
  <strong>{{ followers }}</strong>
  <button @click="followUser">Follow</button>
</template>

<script>
export default {
  name: "App",
  data() {
    return {
      followers: 0,
      user: {
        id: 1,
        username: "FoxLegion",
        firstName: "Gerson",
        lastName: "Severo",
        email: "gersonsevero90@gmail.com",
        isAdmin: true,
      },
    };
  },
  computed: {
    fullName() {
      return `${this.user.firstName} ${this.user.lastName}`;
    },
  },
  methods: {
    followUser() {
      this.followers++;
    },
  },
};
</script>

